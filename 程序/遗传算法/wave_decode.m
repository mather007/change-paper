function [every_thr_len] = wave_decode(c_max_min)
%-------------------------------------------------------------------------
%   WAVE_DECODE返回各层的阈值各在二进制编码中占几位
%-------------------------------------------------------------------------
%第一个参数是指各层系数绝对值的最大值最小值
%回传的参数是各层的阈值各在二进制编码中占几位

%首先精度是小数点后4位
%确定数串位数用2^(m-1)<（U-L）*10^4<=2^(m)-1
g = (c_max_min(:,1)-c_max_min(:,2)).*10^4;
g_1 = floor(log2(g)+1);%向负无穷方向取整
every_thr_len = g_1';
end