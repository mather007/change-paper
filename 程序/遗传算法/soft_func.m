function [c2]=soft_func(a,b)
%-------------------------------------------------------------------------
%   返回经软处理后的各层系数
%-------------------------------------------------------------------------
c=a;%   a是高频部分的各层系数
thr1=b;%b各层阈值
[m,n] = size(a);
c2 = zeros(m,n);
for i = 1:m
    c2(i,:)=(sign(c(i,:)).*(abs(c(i,:))-thr1(i))).*(abs(c(i,:))>=thr1(i))+0.*(abs(c(i,:))<thr1(i));
end
end